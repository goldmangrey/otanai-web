function matchesPracticalLegalPattern(text) {
  return PRACTICAL_LEGAL_PATTERNS.some((pattern) =>
    pattern.every((term) => text.includes(term))
  )
}

export function isLegalResearchQuery(message) {
  return shouldUseNpaAssistant(message)
}

export function shouldUseNpaAssistant(message) {
  const text = String(message || '').toLocaleLowerCase('ru')
  if (!text.trim()) return false
  if (NON_LEGAL_HINTS.some((term) => text.includes(term)) && !LEGAL_CONTEXT_HINTS.some((term) => text.includes(term))) {
    return false
  }
  if (matchesPracticalLegalPattern(text)) {
    return true
  }
  return LEGAL_TERMS.some((term) => text.includes(term))
}
